var timeZero = 1449205200000;  // arbitrary UTC time anchor; selected 12/4/2015 12:00:00 AM EST (12/4/2015 05:00:00 AM GMT)

var oneDay = 86400000; // milliseconds in 1 day, 
var minutesInDay = 24 * 60;

var timeDelta = ((timeNow - timeZero) / oneDay) * minutesInDay; // time passed since time zero, in units of minutes
var selector = Math.floor(timeDelta) % quotes.length;   // round delta down, then find the remainder when dividing by the length of array

$( "#quote" ).append(quotes[selector].text);
$( "#author" ).append(quotes[selector].author);


